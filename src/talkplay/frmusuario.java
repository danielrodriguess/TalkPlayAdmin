    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int linha = tabela1.getSelectedRow();
        String codigo = String.valueOf(tabela1.getValueAt(linha, 0));
        
               
        try{
            PreparedStatement pst1 = banco.con.prepareStatement("select * from nick where email = '"+codigo.toString()+"'");
            banco.rs = pst1.executeQuery();
            if(banco.rs.next()){
              statususer = banco.rs.getString("statususer");
              statusadmin = banco.rs.getString("statusadmin");
            }
            PreparedStatement pst = banco.con.prepareStatement("select * from usuario where email = '"+codigo.toString()+"'");
           banco.rs = pst.executeQuery();
            if(banco.rs.next()){
                
               String nome = banco.rs.getString("nome");
               String email = banco.rs.getString("email");
               String senha = banco.rs.getString("senha");
               String resposta = banco.rs.getString("resposta");
               Date daa = banco.rs.getDate("datahora");
               String dataFormatada = new SimpleDateFormat("dd/MM/yyyy").format(daa);
                JOptionPane.showMessageDialog(null,"Nome: "+nome+"\nEmail: "+email+"\nSenha: "+senha+"\nResposta de Segurança: "+resposta+"\nAtiva pelo usuário? "+statususer+"\nAtivo pelo admin? "+statusadmin+"\nData de cadastro: "+dataFormatada);             
            }
            
        }catch(SQLException ex){
                      
        }
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

