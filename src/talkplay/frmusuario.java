		PreparedStatement pst5 = banco.con.prepareStatement("update ranking set imagem = 'padrao.jpg' where email = '"+codigo.toString()+"'");
		PreparedStatement pst6 = banco.con.prepareStatement("update amigos set status = 'Não' where nickremetente = '"+nick.toString()+"'");
		PreparedStatement pst7 = banco.con.prepareStatement("update amigos set status = 'Não' where nickdestinatario = '"+nick.toString()+"'");
                pst1.executeUpdate();
